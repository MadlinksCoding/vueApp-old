function normalizeBookingValidationCode(value) {
  return typeof value === "string" ? value.trim().toLowerCase() : "";
}

export function resolveBookingValidationTranslationKey(value) {
  const code = typeof value === "string" ? value : value?.code;
  return bookingValidationCodeTranslationKeys[normalizeBookingValidationCode(code)] || "";
}

export function translateBookingValidationError(error, t = bookingT) {
  if (typeof error === "string") return t(resolveBookingValidationTranslationKey(error) || error);
  const key = error?.translationKey || resolveBookingValidationTranslationKey(error) || error?.code;
  if (key) return t(key, error?.params || {});
  return error?.message || t("common_error");
}

export function formatBookingValidationErrors(errors = [], t = bookingT) {
  const list = Array.isArray(errors) ? errors : [];
  return list.map((error) => translateBookingValidationError(error, t));
}

function firstArray(...values) {
  return values.find((value) => Array.isArray(value)) || [];
}

function extractCreateEventValidationDetails(errorLike = {}) {
  return firstArray(
    errorLike,
    errorLike?.details,
    errorLike?.details?.details,
    errorLike?.details?.errors,
    errorLike?.error?.details,
    errorLike?.error?.details?.details,
    errorLike?.error?.details?.errors,
  );
}

function translateCreateEventValidationDetail(detail, t = bookingT) {
  if (!detail || typeof detail !== "object") return "";

  if (detail.field === "bookingBufferMinutes") {
    return t("booking_validation_buffer_time_min");
  }

  const key = detail.translationKey || detail.code;
  if (typeof key === "string" && key.startsWith("booking_validation_")) {
    return t(key, detail.params || {});
  }

  return "";
}

function isEventLimitReachedError(value) {
  return String(value || "").trim().toLowerCase() === "event limit reached";
}

function getCreateEventEventLimitInfo(errorLike = {}) {
  const candidates = [
    errorLike,
    errorLike?.details,
    errorLike?.details?.details,
    errorLike?.error,
    errorLike?.error?.details,
    errorLike?.error?.details?.details,
  ];

  const limitSource = candidates.find((candidate) => (
    candidate
    && typeof candidate === "object"
    && (
      isEventLimitReachedError(candidate.error)
      || isEventLimitReachedError(candidate.message)
    )
  ));

  if (!limitSource) return null;

  const details = limitSource.details && typeof limitSource.details === "object"
    ? limitSource.details
    : {};
  const nestedDetails = details.details && typeof details.details === "object"
    ? details.details
    : {};

  return {
    limit: nestedDetails.limit ?? details.limit ?? limitSource.limit ?? "",
    current: nestedDetails.current ?? details.current ?? limitSource.current ?? "",
  };
}

export function formatCreateEventValidationErrors(errorLike = {}, t = bookingT) {
  const details = extractCreateEventValidationDetails(errorLike);
  const messages = details
    .map((detail) => translateCreateEventValidationDetail(detail, t))
    .filter(Boolean);

  return [...new Set(messages)];
}

export function formatCreateEventFailureMessage(errorLike = {}, t = bookingT) {
  const validationMessages = formatCreateEventValidationErrors(errorLike, t);
  if (validationMessages.length > 0) {
    return validationMessages.join(" ");
  }

  const eventLimitInfo = getCreateEventEventLimitInfo(errorLike);
  if (eventLimitInfo) {
    return t("booking_event_limit_reached_message", eventLimitInfo);
  }

  const backendMessage = extractBackendErrorMessage(errorLike, {
    ignoredMessages: [
      t("booking_create_failed_message"),
      "Failed to create event.",
      "Unexpected error while creating event.",
    ],
  });
  if (backendMessage) return backendMessage;

  const fallbackCode = errorLike?.error?.code || errorLike?.code || "";
  if (fallbackCode) {
    return `Backend rejected the event request (${fallbackCode}).`;
  }

  return t("booking_create_failed_message");
}
