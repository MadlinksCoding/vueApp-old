function interpolate(message, params = {}) {
  if (!params || typeof params !== "object") return message;
  return String(message).replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) => {
    const value = params[key];
    return value === null || value === undefined ? match : String(value);
  });
}

export function normalizeBookingTranslations(input = {}) {
  if (!input || typeof input !== "object" || Array.isArray(input)) return {};

  return Object.keys(input).reduce((acc, key) => {
    const normalizedKey = String(key || "").trim();
    const value = input[key];
    if (!normalizedKey || typeof value !== "string") return acc;
    acc[normalizedKey] = value;
    return acc;
  }, {});
}

export function normalizeBookingLocale(value) {
  const normalized = typeof value === "string" ? value.trim() : "";
  return normalized || "en";
}

export function createBookingTranslator(options = {}) {
  const state = reactive({
    locale: normalizeBookingLocale(options.locale),
    translations: normalizeBookingTranslations(options.translations),
  });

  const t = (key, params = {}) => {
    const normalizedKey = String(key || "").trim();
    if (!normalizedKey) return "";
    const message = state.translations[normalizedKey] || bookingMessages[normalizedKey] || normalizedKey;
    return interpolate(message, params);
  };

  const setTranslations = (translations = {}) => {
    state.translations = normalizeBookingTranslations(translations);
  };

  const setLocale = (locale = "en") => {
    state.locale = normalizeBookingLocale(locale);
  };

  return {
    state,
    locale: computed(() => state.locale),
    translations: computed(() => state.translations),
    t,
    setLocale,
    setTranslations,
  };
}

const fallbackTranslator = createBookingTranslator(fallbackState);

export function provideBookingTranslations(source = {}) {
  const translator = createBookingTranslator({
    locale: source.locale,
    translations: source.translations,
  });

  if (source && typeof source === "object") {
    watch(
      () => source.locale,
      (locale) => translator.setLocale(locale),
      { immediate: true },
    );

    watch(
      () => source.translations,
      (translations) => translator.setTranslations(translations),
      { immediate: true, deep: true },
    );
  }

  provide(bookingTranslationSymbol, translator);
  return translator;
}

export function useBookingTranslations() {
  return inject(bookingTranslationSymbol, fallbackTranslator);
}

export function bookingT(key, params = {}) {
  return fallbackTranslator.t(key, params);
}

